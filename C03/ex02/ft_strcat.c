char	*ft_strcat(char *dest, char *src)
{
	int	j;

	while (*dest)
		dest++;
	j = 0;
	while (src[j])
	{
		*dest = src[j];
		dest++;
		j++;
	}
	*dest = 0;
	return (dest);
}