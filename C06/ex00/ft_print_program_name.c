#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	*str;

	if (!argc)
		return (0);
	str = *argv;
	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
